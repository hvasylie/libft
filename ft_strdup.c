#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*str;
	size_t	len;

	len = ft_strlen((char*)s1) + 1;
	i = 0;
	str = (char *)malloc(sizeof(char) * len);
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
